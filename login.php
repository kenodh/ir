	function onLoginClick() {
		if (validateText(document.frmLogin.txtUserName.value,5,0) == false) { 
			alert('Invalid username or password');
			return; 
		}
		else {
			if (document.frmLogin.txtUserName.value.toUpperCase() != 'ADMINISTRATOR') {
				if (validateText(document.frmLogin.txtPassword.value,5,0) == false) {
					alert('Invalid username or password');
					return;
				}
			}
		}
		document.frmLogin.cmdLogin.disabled = true;
		document.frmLogin.submit();
	}
	
	function onPasswordKeyPress(e) {
		var br = detectBrowser();

		if (br == 0)
    	    var Key = window.event.keyCode;
	    else
    	    var Key = e.which;
			
	    if (Key == 13)
    	    onLoginClick();
	}
	
	function window_onLoad()
	{
		document.frmLogin.txtUserName.focus();
	}
	
	if (window.addEventListener)
		window.addEventListener("load", window_onLoad, false)
	else if (window.attachEvent)
		window.attachEvent("onload", window_onLoad)
	else if (document.getElementById)
		window.onload = window_onLoad
